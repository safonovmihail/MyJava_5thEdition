// FirstVariable class
public class FirstVariable {

	/**
	 * @param args
	 */

	// main method
	public static void main(String[] args) {

		// initialised message var as a string and assigned a value
		String message = "Initial Value";

		// assigned a new value to the message var
		message = "Modified Message";

		//
		System.out.println(message);

	} // main method closure

} // FirstVariable class closure
